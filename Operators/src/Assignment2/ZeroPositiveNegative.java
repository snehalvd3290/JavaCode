package Assignment2;

public class ZeroPositiveNegative {
	public static void main(String[] args) {
		int a = 5;
		if (a == 0) {
			System.out.println(a+ " is Zero");
		}
		else
			if(a>0) {
				System.out.println(a+ " is Positive");
			} else {
				System.out.println(a+ " is Negative");
			}
	}
}
